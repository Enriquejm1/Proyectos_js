/**
 * Operadores de comparación
 */

//Operador igual (==)
console.log(3==3);//Comparando elementos del mismo tipo de dato
console.log(3=='3');//Comparando elementos de diferente tipo de dato

//Operador no es igual (!=)
console.log(3!=3);
console.log(3!=2);

//Operador estrictamente igual (===)
console.log(3===3);
console.log(3==='3');

//Operador desigual estricto (!==)
console.log(3!==3);
console.log(3!==4);

//Operado menor que
console.log(3<4);

//Operado mayor que
console.log(3>4);

//Operador menor o igual que
console.log(10<=4);

//Operador meyor o igual que
console.log(4>=2);

