/**
 * Operadores Aritmeticos
 *  + . - , / , *, %
 * 
 * 
 */

console.log("La suma es:"+2+2);
console.log("La resta es:"+(2-2));
console.log("La Multiplicación es:"+2*2);
console.log("La Divición es:"+(2/2));
console.log("La potenciación es:"+2**2);

//Operadores de incremento (++)
let num = 10;
console.log(++num); //El incremento se realiza primero y despues imprime el valor de la variable
console.log(num++); // Primero se imprime el valor de la variable y despues se incrementa
console.log(num);

//Operador de decremento (--)
num = 3;
console.log(num--);
console.log(num);









